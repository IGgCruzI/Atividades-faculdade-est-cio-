# 6 - Faça um algoritmo que calcule a área de um retângulo. Posteriormente imprima o resultado.

print("Seja bem vindo ao sistema de cálculo da área de um retângulo.\n")

altura =int(input("Informe a altura do retângulo.\n"))

base = int(input("Agora informe a base do retângulo.\n"))

area = (altura*base)

print(f"A base do seu retângulo é {area}.")