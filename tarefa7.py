# 7 - Faça um algoritmo que calcule a área de um triângulo. Posteriormente imprima o resultado.

print("Seja bem vindo ao sistema de cálculo da área de um triângulo.\n")

altura =int(input("Informe a altura do triângulo.\n"))

base = int(input("Agora informe a base do triângulo.\n"))

area = (altura*base)

print(f"A base do seu triângulo é {area}.")